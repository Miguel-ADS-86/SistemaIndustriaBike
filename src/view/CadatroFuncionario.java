    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        // cadastrar Departamento
        Dao_Departamento dao = new Dao_Departamento();
        Departamento departamento = new Departamento();
      
        if(!jtextNome.getText().trim().equals("") && !jtextNomeSetor.getText().trim().equals("")){
         String nome = jtextNome.getText();
         String setor = jtextNomeSetor.getText();      
         departamento.setNome(nome);
         departamento.setSetor(setor);
            try {
                dao.cadastrarDepartamento(departamento);
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            
        jtextNome.setText("");
        jtextNomeSetor.setText("");
        }else{
         JOptionPane.showMessageDialog(null, "Todos os campos precisam ser preenchidos");
        }
        
    }//GEN-LAST:event_jbCadastrarActionPerformed


