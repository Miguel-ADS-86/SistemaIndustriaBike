     public void listar() throws Exception{
        Dao_Departamento dao = new Dao_Departamento();
        ArrayList<Departamento> dps = dao.listarDepartamento();
        ajustarTabela();//metodo que criei para poder ajustar as colunas da tabela.
        //eu crio um Tablemodel e passo o nome databela fazendo um cast para o tableModel
        DefaultTableModel model = (DefaultTableModel)TabelaDepartamento.getModel(); 
        model.setNumRows(0);//digo que vai iniciar com 0 linhas
        
        for(Departamento d: dps){
            model.addRow(new Object[]{
            d.getCodigo(),
            d.getNome(),
            d.getSetor()
            });
        }
        
    }
