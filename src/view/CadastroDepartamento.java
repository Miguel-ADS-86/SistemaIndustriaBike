    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        // pesquisar pelo nome do departamento
        String nome = jtextNome.getText();
        String setor = jtextNomeSetor.getText();
        ArrayList<Departamento> departamentos = new ArrayList<Departamento>();
        if(!nome.trim().equals("") || 
                !setor.trim().equals("")){
            try{
            Dao_Departamento dao = new Dao_Departamento();
            Departamento departamento = new Departamento();
            departamento.setNome(nome);
            departamento.setSetor(setor);
            departamentos = dao.pesquisarDepartamento(departamento);
            
            DefaultTableModel model = (DefaultTableModel)TabelaDepartamento.getModel();
            model.setNumRows(0);
            for(Departamento d:departamentos){
                model.addRow(new Object[]{
                d.getCodigo(),
                d.getNome(),
                d.getSetor()
                });
            }
            }catch(Exception e){
                e.printStackTrace();
            }
        
        }else{
          JOptionPane.showMessageDialog(null, "campos vazio não pode ser pesquisado!");
        }
    }//GEN-LAST:event_jbPesquisarActionPerformed

