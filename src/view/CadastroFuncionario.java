    //metodo para carregar os dados do banco na tababela
    public void listar() {
        try {
            Dao_Funcionario d = new Dao_Funcionario();
            ArrayList<Funcionario> objs = d.listarFuncionario();
            DefaultTableModel model = (DefaultTableModel) TabelaFuncionario.getModel();
            model.setNumRows(0);// vai iniciar com 0 linhas
            //percorrer a lista de objetos
            for(Funcionario f: objs){
                model.addRow(new Object[]{
                   f.getCodigo(),f.getNome(),f.getTelefone(),
                   f.getLogin(),f.getSenha(),f.getCodigo_Departamento().getNome(),
                   f.getCodigo_Departamento().getSetor()   
                });
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

