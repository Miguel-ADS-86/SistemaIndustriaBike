    private void TabelaFuncionarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaFuncionarioMouseClicked
        //pegando o valot atravez do click
        jtextCodigo.setText(TabelaFuncionario.getValueAt
        (TabelaFuncionario.getSelectedRow(),0).toString());
        jtextNome.setText(TabelaFuncionario.getValueAt(
                TabelaFuncionario.getSelectedRow(),1).toString());
        jtextTelefone.setText(TabelaFuncionario.getValueAt(
                TabelaFuncionario.getSelectedRow(),2).toString());
        jtextLogin.setText(TabelaFuncionario.getValueAt(
                TabelaFuncionario.getSelectedRow(),3).toString());
        jpSenha.setText(TabelaFuncionario.getValueAt(
                TabelaFuncionario.getSelectedRow(),4).toString());
        
        //pegar o valor da tabela e jogar na combobox que é um array
        String model = TabelaFuncionario.getValueAt(
                TabelaFuncionario.getSelectedRow(),5).toString();
        for(int i=0; i < jcDepartamento.getItemCount();i++){
            if(jcDepartamento.getItemAt(i).toString().equalsIgnoreCase(model)){
               jcDepartamento.setSelectedIndex(i);
            }
        }
        
        textSetor.setText(TabelaFuncionario.getValueAt(
                TabelaFuncionario.getSelectedRow(),6).toString());
    }//GEN-LAST:event_TabelaFuncionarioMouseClicked


