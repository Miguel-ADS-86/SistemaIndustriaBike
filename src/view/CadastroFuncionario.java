    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        // botão cadastrar Funcionario 
        try {
            Dao_Funcionario dao = new Dao_Funcionario();
            Funcionario funcionario = new Funcionario();
            funcionario.setNome(jtextNome.getText());
            funcionario.setTelefone(jtextTelefone.getText());
            funcionario.setLogin(jtextLogin.getText());
            funcionario.setSenha(jpSenha.getText());

            Departamento departamento = (Departamento) jcDepartamento.getSelectedItem();
            funcionario.setCodigo_Departamento(departamento);
            dao.cadastrarFuncionario(funcionario);
            listar();
            jtextCodigo.setText("");
            jtextNome.setText("");
            jtextTelefone.setText("");
            jtextLogin.setText("");
            jpSenha.setText("");
            
         
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jbCadastrarActionPerformed

